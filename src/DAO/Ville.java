package DAO;

public class Ville {
	
	private float CODGEO;
	private int REG; 
	private float DEP; 
	private int ZE2010;
	private String LIBGEO;
	private float SUPERF;

	
	public float getCODGEO() {
		return CODGEO;
	}
	public void setCODGEO(float cODGEO) {
		CODGEO = cODGEO;
	}
	public int getREG() {
		return REG;
	}
	public void setREG(int rEG) {
		REG = rEG;
	}
	public float getDEP() {
		return DEP;
	}
	public void setDEP(float dEP) {
		DEP = dEP;
	}
	public int getZE2010() {
		return ZE2010;
	}
	public void setZE2010(int zE2010) {
		ZE2010 = zE2010;
	}
	public String getLIBGEO() {
		return LIBGEO;
	}
	public void setLIBGEO(String lIBGEO) {
		LIBGEO = lIBGEO;
	}
	public float getSUPERF() {
		return SUPERF;
	}
	public void setSUPERF(float sUPERF) {
		SUPERF = sUPERF;
	}

}
